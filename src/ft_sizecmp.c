int	ft_sizecmp(const char *str1, const char *str2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str1[i] != '\0' && str2[j] != '\0')
	{
		i++;
		j++;
		if (str1[i] == '\0' && str2[j] == '\0')
			return (0);
		else if (str1[i] == '\0')
			return (2);
		else if (str2[j] == '\0')
			return (1);
	}
	return (-1);
}
