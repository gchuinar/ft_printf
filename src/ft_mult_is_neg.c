int	ft_mult_is_neg(char *nb1, char *nb2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while ((((nb1[i] > 8 && nb1[i] < 14) || nb1[i] == ' ')))
		i++;
	while ((((nb2[j] > 8 && nb2[j] < 14) || nb2[j] == ' ')))
		j++;
	if (nb1[i] == '-' && nb2[j] == '-')
		return (0);
	else if (nb1[i] == '-' || nb2[j] == '-')
		return (1);
	return (0);
}
