int	ft_mult_is_neg(char **nb1, char **nb2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while ((((**nb1 > 8 && **nb1 < 14) || **nb1 == ' ')))
	{
		(*nb1)++;
	}
	while ((((**nb2 > 8 && **nb2 < 14) || **nb2 == ' ')))
		(*nb2)++;
	if (**nb1 == '-' && **nb2 == '-')
	{
		(*nb1)++;
		(*nb2)++;
		return (0);
	}
	else if (**nb1 == '-')
	{
		(*nb1)++;
		return (1);
	}
	else if (**nb2 == '-')
	{
		(*nb2)++;
		return (1);
	}
	return (0);
}
